    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      try
        {
             if(Integer.parseInt(eodo.getText())<Integer.parseInt(sodo.getText()))
                throw new Exception("End Odometer should be greater than Start Odometer");
            Connection c=MyConnection.createCon();
            PreparedStatement p=c.prepareStatement("insert into tripcompletion values(?,?,?,?,?,?,?,?)");
            p.setString(1, resno.getText().trim());
            p.setString(2, staffid.getText().trim());
            p.setString(3, vno.getText().trim());
            p.setString(4, cc.getText().trim());
            p.setFloat(5, Float.parseFloat(fuel1.getText().trim()));
            p.setString(6, sodo.getText().trim());
            p.setString(7, eodo.getText().trim());
            p.setString(8, complaints.getText().trim());
            int rows=p.executeUpdate();
            if(rows==0)
              throw new Exception("Failed!");
            else
              JOptionPane.showMessageDialog(null,"Done!!");
            
        }
        catch(Exception e)
        {
            if(e.getMessage().substring(0, 6).equals("Cannot"))
              JOptionPane.showMessageDialog(null, "Enter Valid Reservation Number");
            else
            JOptionPane.showMessageDialog(null, e.getMessage());
        }   // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

