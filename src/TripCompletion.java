    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
       try
        {
            if(Integer.parseInt(eodo1.getText())<Integer.parseInt(sodo1.getText()))
                throw new Exception("End Odometer should be greater than Start Odometer");
            Connection c=MyConnection.createCon();
            PreparedStatement p=c.prepareStatement("update tripcompletion set credit_card=?,fuel=?, s_odo=?, e_odo=?,complaints=? where res_no=?");
            p.setString(2,fuel.getText());
            p.setString(1,cc1.getText());
            p.setString(3,sodo1.getText());
            p.setString(4,eodo1.getText());
            p.setString(5,complaints1.getText());
            p.setString(6,resno1.getText());
            
            int rows=p.executeUpdate();
            if(rows==0)
            throw new Exception("Failed!");
            else
            JOptionPane.showMessageDialog(null,"Updated" );
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        } // TODO add your handling code here:
    }//GEN-LAST:event_updateActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        try
        {
            Connection c=MyConnection.createCon();
            PreparedStatement p1=c.prepareStatement("select * from tripcompletion where res_no=?");
            p1.setString(1, delres.getText().trim());
            ResultSet rr=p1.executeQuery();
            if(rr.next()==false)
            throw new Exception("Not Found!!");
            PreparedStatement p=c.prepareStatement("delete from  tripcompletion where res_no=?");
            p.setString(1, delres.getText().trim());
            int rows=p.executeUpdate();
            if(rows==0)
            throw new Exception("Failed!");
            else
            del.setText("Deleted!!");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        } // TODO add your handling code here:
    }//GEN-LAST:event_deleteActionPerformed

