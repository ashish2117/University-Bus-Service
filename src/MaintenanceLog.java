    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
         try
        {
            Connection c=MyConnection.createCon();
            PreparedStatement p=c.prepareStatement("insert into maintenancelog(v_no,log_date,description,staff_id) values(?,?,?,?);");
            p.setString(1, vid.getText().trim());
            p.setString(2, date.getText().trim());
            p.setString(3, desc.getText());
            p.setString(4, staffid.getText().trim());
            int rows=p.executeUpdate();
            if(rows==0)
            throw new Exception("Failed!");
            else
            {
                Statement p1=c.createStatement();
                ResultSet rr=p1.executeQuery("select max(log_no) from maintenancelog");
                rr.next();
                String ss=rr.getString(1);
                JOptionPane.showMessageDialog(null,"New log added,Your log number is:"+ss );
            }
        }
        catch(Exception e)
        {
            if(e.getMessage().substring(0, 6).equals("Cannot"))
              JOptionPane.showMessageDialog(null, "No Such Staff Id or Vehicle Id exists");
            else
            JOptionPane.showMessageDialog(null, e.getMessage());
        }// TODO add your handling code here:
    }//GEN-LAST:event_submitActionPerformed

