    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        try
        {
            Connection c=MyConnection.createCon();
            PreparedStatement p1=c.prepareStatement("select * from maintenancelog where log_no=?");
            p1.setString(1, Logno2.getText().trim());
            ResultSet rr=p1.executeQuery();
            if(rr.next()==false)
            throw new Exception("Not Found!!");
            PreparedStatement p2=c.prepareStatement("delete from partsused where log_no=?");
            PreparedStatement p3=c.prepareStatement("delete from maintenancedetail where log_no=?");
            PreparedStatement p=c.prepareStatement("delete from maintenancelog where log_no=?");
            p2.setString(1, Logno2.getText().trim());
            p3.setString(1, Logno2.getText().trim());
            p.setString(1, Logno2.getText().trim());
            p2.executeUpdate();
            p3.executeUpdate();
            int rows=p.executeUpdate();
            if(rows==0)
            throw new Exception("Failed!");
            else
            JOptionPane.showMessageDialog(null,"Deleted" );
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        } // TODO add your handling code here:
    }//GEN-LAST:event_deleteActionPerformed

